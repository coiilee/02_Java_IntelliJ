package com.kh.oop.methodPre;

public class PersonPre {

    // Private 멤버 변수
    // 이름 나이 만들기
    // setter getter 설정 하기

    private String name ;
    private int age ;


    public void setName(String name) {
        this.name = name;
    }
    public String getName() {
        return name;
    }

    public void setAge(int age) {
        this.age = age;
    }
    public int getAge() {
        return age;
    }


}
